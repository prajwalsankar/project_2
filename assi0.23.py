#WAP to perform simple calculations
a=int(input("Enter value a:"))
b=int(input("Enter value b:"))
i=input("Enter the operator('+','-','*','//','%','/')")
if i=='+':
    print("a+b is",a+b)
elif i=='-':
    print("a-b is",a-b)
elif i=='*':
    print("a*b is",a*b)
elif i=='//':
    print("a//b is",a//b)
elif i=='%':
    print("a%b is",a%b)
elif i=='/':
    print("a/b is",a/b)
